import difflib
def only_insertions(orig, new, label):
    sm = difflib.SequenceMatcher(None, orig.splitlines(), new.splitlines())
    removed = [orig.splitlines()[i] for tag, i1, i2, j1, j2 in sm.get_opcodes() if tag in ("delete",) for i in range(i1, i2)]
    replaced = [(orig.splitlines()[i1:i2], new.splitlines()[j1:j2]) for tag, i1, i2, j1, j2 in sm.get_opcodes() if tag == "replace"]
    print(f"{label}: 削除行={len(removed)} 置換ブロック={len(replaced)}")
    for o, n in replaced:
        print("  置換前:", o[0][:60], "→ 置換後:", n[0][:60])

