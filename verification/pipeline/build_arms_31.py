def insert_after(text, anchor, addition, label):
    assert text.count(anchor) == 1, f"アンカー非一意: {label} ({text.count(anchor)}件)"
    return text.replace(anchor, anchor + "\n" + addition)

def append_to(text, anchor, addition, label):
    assert text.count(anchor) == 1, f"アンカー非一意: {label} ({text.count(anchor)}件)"
    return text.replace(anchor, anchor + addition)

